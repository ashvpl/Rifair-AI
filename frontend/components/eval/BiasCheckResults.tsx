'use client';

import { useState, useEffect } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { Lock, Check, X, RefreshCw, ArrowRight, AlertTriangle } from 'lucide-react';
import { cn } from '@/lib/utils';

interface BiasResult {
  score: number;
  tags: string[];
  oneLiner: string;
  explanation: string;
  rewrite?: string | undefined; // Only populated for paid users
}

interface Props {
  questions: string[];
  biasResults: BiasResult[];
  planTier: string; // 'free' | 'starter' | 'growth'
  evalId: string;
  onQuestionsUpdate: (newQuestions: string[]) => void;
}

export default function BiasCheckResults({ 
  questions, 
  biasResults, 
  planTier, 
  evalId,
  onQuestionsUpdate 
}: Props) {
  const [localQuestions, setLocalQuestions] = useState(questions);
  const [localBiasResults, setLocalBiasResults] = useState(biasResults);
  const [rewrites, setRewrites] = useState<Record<number, string>>({});
  const [loadingRewrite, setLoadingRewrite] = useState<Record<number, boolean>>({});
  const [rateLimitMessage, setRateLimitMessage] = useState<Record<number, string>>({});

  useEffect(() => {
    setLocalQuestions(questions);
  }, [questions]);

  useEffect(() => {
    setLocalBiasResults(biasResults);
  }, [biasResults]);

  const isPaid = planTier !== 'free' && planTier !== 'none';
  const flaggedCount = localBiasResults.filter(b => b.score > 50).length;

  // Auto-fetch missing rewrites on mount
  useEffect(() => {
    if (!isPaid || !evalId || evalId === 'undefined') return;
    
    biasResults.forEach((bias, i) => {
      if (bias.score > 50 && !bias.rewrite && !rewrites[i] && !loadingRewrite[i]) {
        handleRegenerateRewrite(i);
      }
    });
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [biasResults, isPaid, evalId]);

  const handleUseRewrite = (index: number) => {
    if (!isPaid) return;
    
    const rewriteToUse = rewrites[index] || localBiasResults[index].rewrite;
    if (!rewriteToUse) return;

    const updated = [...localQuestions];
    updated[index] = rewriteToUse;
    setLocalQuestions(updated);
    onQuestionsUpdate(updated);

    // Clear the bias result for this question since it's rewritten
    setLocalBiasResults(prev => {
      const next = [...prev];
      next[index] = {
        ...next[index],
        score: 0,
        tags: [],
        explanation: "",
        oneLiner: "Fair version applied",
        rewrite: undefined // Also clear the suggestion
      };
      return next;
    });
    
    // Clear the rewrite state for this question
    setRewrites(prev => {
      const next = { ...prev };
      delete next[index];
      return next;
    });
  };

  const handleRegenerateRewrite = async (index: number) => {
    // Robust guard against missing or malformed evalId
    const isValidId = evalId && typeof evalId === 'string' && evalId !== 'undefined' && evalId.length > 10;
    
    if (!isPaid || !isValidId) {
      console.warn('[BiasCheck] Skipping rewrite: invalid state', { isPaid, evalId, index });
      return;
    }
    
    setLoadingRewrite(prev => ({ ...prev, [index]: true }));
    
    try {
      const res = await fetch(`/api/evaluations/${evalId}/rewrite`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ 
          questionIndex: index,
          question: localQuestions[index],
          previousRewrite: rewrites[index] || localBiasResults[index].rewrite
        })
      });
      
      const data = await res.json();
      
      if (!res.ok) {
        if (res.status === 429 && data.error === 'RATE_LIMITED') {
          setRateLimitMessage(prev => ({ 
            ...prev, 
            [index]: `You're refining this question quickly. Take a moment to review the options you have. [Rewrites available again in ${data.resetInMinutes} minutes]` 
          }));
          return;
        }
        throw new Error(data.error || data.message || `HTTP ${res.status}`);
      }
 
      if (data.rewrite) {
        setRewrites(prev => ({ ...prev, [index]: data.rewrite }));
        setRateLimitMessage(prev => {
          const next = { ...prev };
          delete next[index];
          return next;
        });
      } else {
        throw new Error('No rewrite generated by AI');
      }
    } catch (err: any) {
      console.error('Failed to regenerate rewrite:', err);
      setRewrites(prev => ({ ...prev, [index]: `Error: ${err.message}` }));
    } finally {
      setLoadingRewrite(prev => ({ ...prev, [index]: false }));
    }
  };

  const handleKeepOriginal = (index: number) => {
    // Hide the rewrite suggestion by clearing it from local state
    setRewrites(prev => {
      const next = { ...prev };
      delete next[index];
      return next;
    });
    setLocalBiasResults(prev => {
      const next = [...prev];
      next[index] = {
        ...next[index],
        rewrite: undefined
      };
      return next;
    });
  };

  return (
    <div className="w-full max-w-2xl mx-auto px-4 py-6 space-y-4">
      {/* Header Alert */}
      {flaggedCount > 0 && (
        <div className="bg-amber-50 border border-amber-200 rounded-2xl p-4 flex items-start gap-3">
          <div className="shrink-0 w-10 h-10 rounded-full bg-amber-100 flex items-center justify-center">
            <AlertTriangle className="w-5 h-5 text-amber-600" />
          </div>
          <div>
            <h3 className="text-sm font-bold text-amber-900">
              {flaggedCount} question{flaggedCount > 1 ? 's' : ''} flagged for potential bias
            </h3>
            <p className="text-sm text-amber-700 mt-1">
              Review issues below before evaluating.
            </p>
          </div>
        </div>
      )}

      {/* Question Cards */}
      {localQuestions.map((question, i) => {
        const bias = localBiasResults[i];
        if (!bias) return null;
        
        const isFlagged = bias.score > 50;
        const currentRewrite = rewrites[i] || bias.rewrite;
        const isRewritten = localQuestions[i] !== questions[i];

        return (
          <div 
            key={i} 
            className={cn(
              "bg-white rounded-2xl border overflow-hidden transition-all",
              isFlagged ? "border-amber-200" : "border-neutral-200",
              isRewritten && "ring-2 ring-emerald-200"
            )}
          >
            {/* Question Header */}
            <div className="p-4">
              <div className="flex items-start gap-3">
                <span className={cn(
                  "shrink-0 w-7 h-7 rounded-full flex items-center justify-center text-xs font-bold",
                  isFlagged ? "bg-amber-100 text-amber-700" : "bg-emerald-100 text-emerald-700"
                )}>
                  {isFlagged ? '!' : '✓'}
                </span>
                <div className="flex-1 min-w-0">
                  <p className={cn(
                    "text-[15px] font-medium leading-snug",
                    isRewritten ? "text-emerald-900" : "text-neutral-900"
                  )}>
                    {localQuestions[i]}
                  </p>
                  {isRewritten && (
                    <span className="inline-flex items-center gap-1 mt-1 text-[10px] font-bold text-emerald-700 uppercase tracking-wide">
                      <Check className="w-3 h-3" /> AI Rewritten
                    </span>
                  )}
                  
                  {/* Tags */}
                  <div className="flex items-center gap-2 mt-2 flex-wrap">
                    <span className={cn(
                      "text-xs font-bold px-2 py-0.5 rounded-full",
                      bias.score > 70 ? "bg-red-100 text-red-700" :
                      bias.score > 50 ? "bg-amber-100 text-amber-700" : "bg-emerald-100 text-emerald-700"
                    )}>
                      {bias.score}/100
                    </span>
                    {bias.tags.map(tag => (
                      <span key={tag} className="text-[10px] uppercase tracking-wide text-neutral-500 bg-neutral-100 px-1.5 py-0.5 rounded">
                        {tag}
                      </span>
                    ))}
                  </div>
                </div>
              </div>
            </div>

            {/* Issue Explanation */}
            {isFlagged && (
              <div className="px-4 pb-3">
                <div className="ml-10 p-3 bg-red-50/50 rounded-xl border border-red-100">
                  <p className="text-xs font-semibold text-red-700 mb-1">Issue detected</p>
                  <p className="text-sm text-red-900 leading-relaxed">{bias.explanation}</p>
                </div>
              </div>
            )}

            {/* AI Rewrite Section */}
            {isFlagged && (currentRewrite || !isPaid) && (
              <div className="px-4 pb-4">
                <div className="ml-10">
                  {!isPaid ? (
                    /* FREE: Locked */
                    <div className="relative p-4 bg-red-50/30 rounded-xl border border-red-100/50">
                      <p className="text-xs font-semibold text-red-700 uppercase tracking-wide mb-2">AI Suggestion</p>
                      <p className="text-sm text-red-800/60 italic">
                        "Consider focusing on the underlying competency..."
                      </p>
                      
                      <div className="mt-3 p-3 bg-white rounded-lg border border-neutral-200">
                        <div className="flex items-center gap-2 mb-2">
                          <Lock className="w-4 h-4 text-neutral-400" />
                          <p className="text-xs font-medium text-neutral-600">Full rewrite in Starter</p>
                        </div>
                        <a href="/pricing?reason=rewrite" className="block w-full text-center py-2 bg-[#dc2626] text-white text-xs font-semibold rounded-lg hover:bg-red-700 transition-colors">
                          Unlock rewrites — ₹999
                        </a>
                      </div>
                    </div>
                  ) : (
                    /* PAID: Full rewrite with actions */
                    <div className="p-4 bg-red-50 rounded-xl border border-red-100">
                      <p className="text-xs font-semibold text-red-700 uppercase tracking-wide mb-2">AI Rewrite</p>
                      
                      {rateLimitMessage[i] && (
                        <div className="mb-3 p-3 bg-amber-50 rounded-lg border border-amber-200">
                          <p className="text-sm text-amber-800 font-medium">
                            {rateLimitMessage[i]}
                          </p>
                        </div>
                      )}

                      {loadingRewrite[i] ? (
                        <div className="h-12 flex items-center justify-center">
                          <RefreshCw className="w-4 h-4 text-red-600 animate-spin" />
                        </div>
                      ) : (
                          <p className="text-sm text-red-900 leading-relaxed">
                            {currentRewrite || "AI is generating a fairer version..."}
                          </p>
                        )}
                        
                        {!isRewritten && (
                          <div className="flex items-center gap-2 mt-3">
                            <button
                              onClick={() => handleUseRewrite(i)}
                              className="flex-1 flex items-center justify-center gap-1.5 py-2 bg-[#dc2626] text-white text-xs font-semibold rounded-lg active:scale-[0.98] transition-transform touch-target"
                            >
                              <Check className="w-3.5 h-3.5" /> Use this rewrite
                            </button>
                            <button
                              onClick={() => handleRegenerateRewrite(i)}
                              disabled={loadingRewrite[i]}
                              className="flex items-center justify-center gap-1.5 py-2 px-3 border border-red-200 text-red-700 text-xs font-medium rounded-lg active:bg-red-100 touch-target disabled:opacity-50"
                            >
                              <RefreshCw className="w-3.5 h-3.5" /> Try another
                            </button>
                            <button
                              onClick={() => handleKeepOriginal(i)}
                              className="flex items-center justify-center gap-1.5 py-2 px-3 border border-neutral-200 text-neutral-600 text-xs font-medium rounded-lg active:bg-neutral-100 touch-target"
                            >
                              <X className="w-3.5 h-3.5" /> Keep original
                            </button>
                          </div>
                        )}
                      </div>
                  )}
                </div>
              </div>
            )}
          </div>
        );
      })}
    </div>
  );
}
